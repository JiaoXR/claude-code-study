// 获取随机单词
export const getRandomWords = (count: number, excludeIds: string[] = []): Word[] => {
  const availableWords = highSchoolWords.filter(word => !excludeIds.includes(word.id));
  const shuffled = [...availableWords].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// 获取错误选项
export const getWrongOptions = (correctWord: Word, count: number = 3): string[] => {
  const otherWords = highSchoolWords.filter(word => 
    word.id !== correctWord.id && 
    word.chinese !== correctWord.chinese
  );
  const shuffled = [...otherWords].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count).map(word => word.chinese);
};